package br.com.cursojava.introducao.arrays;

public class Arrays1 {
    public static void main(String[] args) {
        /**
         * criando inicializando Arrays
         */
        int[] idade = new int[4];
        idade[0] = 12;
        idade[1] = 19;
        idade[2] = 34;
        idade[3] = 44;

        System.out.println("idade 1: " + idade[0]);
        System.out.println("idade 2: " + idade[1]);
        System.out.println("idade 3: " + idade[2]);
        System.out.println("idade 4: " + idade[3]);

    }
}
